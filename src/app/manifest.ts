import type { MetadataRoute } from "next";

/**
 * Генерирует Web App Manifest.
 *
 * Next.js автоматически отдает этот объект
 * по адресу /manifest.webmanifest.
 *
 * Manifest используется браузерами для:
 * - установки сайта как PWA;
 * - отображения иконок;
 * - настройки режима запуска приложения.
 */
export default function manifest(): MetadataRoute.Manifest {
  return {
    /**
     * Полное название приложения.
     * Показывается при установке PWA.
     */
    name: "Next.js course by TeaCoder",

    /**
     * Короткое название приложения.
     * Используется при ограниченном пространстве
     * (например, под иконкой на телефоне).
     */
    short_name: "TeaCoder",

    /**
     * Страница, открываемая после запуска приложения.
     */
    start_url: "/",

    /**
     * Режим отображения приложения.
     *
     * standalone — приложение выглядит
     * как нативное и открывается
     * без адресной строки браузера.
     */
    display: "standalone",

    /**
     * Набор иконок приложения.
     *
     * Браузер самостоятельно выберет наиболее
     * подходящую иконку в зависимости от устройства.
     */
    icons: [
      {
        /**
         * Путь к иконке.
         */
        src: "/icons/icon-192x192.png",

        /**
         * Размер изображения.
         */
        sizes: "192x192",

        /**
         * MIME-тип файла.
         */
        type: "image/png",

        /**
         * maskable позволяет системе
         * безопасно обрезать иконку
         * под различные формы.
         */
        purpose: "maskable",
      },
      {
        /**
         * Иконка высокого разрешения.
         */
        src: "/icons/icon-512x512.png",

        /**
         * Размер изображения.
         */
        sizes: "512x512",

        /**
         * MIME-тип файла.
         */
        type: "image/png",

        /**
         * Обычная иконка,
         * используемая большинством устройств.
         */
        purpose: "any",
      },
    ],
  };
}

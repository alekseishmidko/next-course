import type { MetadataRoute } from "next";

/**
 * Генерирует файл robots.txt.
 *
 * Next.js автоматически преобразует возвращаемый объект
 * в содержимое robots.txt при обращении к маршруту
 * /robots.txt.
 */
export default function robots(): MetadataRoute.Robots {
  /**
   * Базовый URL сайта.
   *
   * Если переменная окружения отсутствует,
   * используется значение по умолчанию.
   */
  const baseUrl = process.env.NEXT_PUBLIC_SITE_URL || "https://teacoder.ru";

  return {
    /**
     * Правила индексирования для поисковых роботов.
     */
    rules: [
      {
        /**
         * Правило применяется ко всем роботам.
         */
        userAgent: "*",

        /**
         * Разрешенные разделы сайта.
         */
        allow: ["/", "/catalog"],

        /**
         * Запрещенные для индексации разделы.
         */
        disallow: ["/api/", "/admin/", "/private/"],
      },
      {
        /**
         * Отдельные правила для поискового робота Яндекса.
         */
        userAgent: "YandexBot",

        /**
         * Разрешить индексирование всего сайта.
         */
        allow: "/",

        /**
         * Запретить индексирование административной панели.
         */
        disallow: "/admin/",

        /**
         * Минимальная задержка между запросами
         * поискового робота (в секундах).
         */
        crawlDelay: 1,
      },
    ],

    /**
     * Адрес sitemap.xml.
     *
     * Поисковые системы используют его
     * для быстрого обнаружения страниц сайта.
     */
    sitemap: `${baseUrl}/sitemap.xml`,
  };
}
